CREATE PROCEDURE [dbo].[sp_DanhSachDangKy]
AS
BEGIN
    SELECT 
        sv.[HoTen],
        mh.[TenMonHoc],
        dk.[Diem]
    FROM [DangKy] dk
    INNER JOIN [SinhVien] sv 
        ON dk.[SinhVienID] = sv.[SinhVienID]
    INNER JOIN [MonHoc] mh 
        ON dk.[MonHocID] = mh.[MonHocID];
END;
