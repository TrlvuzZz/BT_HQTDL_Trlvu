CREATE FUNCTION [dbo].[fn_XepLoai] (@Diem FLOAT)
RETURNS NVARCHAR(20)
AS
BEGIN
    DECLARE @KQ NVARCHAR(20);

    IF (@Diem >= 8)
        SET @KQ = N'Gioi';
    ELSE IF (@Diem >= 6.5)
        SET @KQ = N'Kha';
    ELSE IF (@Diem >= 5)
        SET @KQ = N'Trung Binh';
    ELSE
        SET @KQ = N'Yeu';

    RETURN @KQ;
END;
GO
